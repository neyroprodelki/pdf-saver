import pyautogui
import time

# ВКЛЮЧАЕМ ТОРМОЗ: если надо остановить — резко ведите мышь в левый верхний угол!
pyautogui.FAILSAFE = True

def main():
    # Небольшое меню в начале
    start = pyautogui.confirm(
        text="Инструкция:\n1. Откройте браузер на первой вкладке.\n2. Убедитесь, что окно сохранения не будет перекрывать другие.\n3. Для остановки — мышь в левый верхний угол экрана.",
        title="PDF-Марафон для Нейропроделок",
        buttons=['Поехали!', 'Отмена']
    )
    
    if start != 'Поехали!':
        return

    print("У вас есть 5 секунд, чтобы переключиться на браузер...")
    time.sleep(5)

    # Делаем один клик в районе центра экрана, чтобы активировать окно браузера
    pyautogui.click() 
    time.sleep(0.5)

    try:
        while True:
            # 1. Жмем Сохранить
            pyautogui.hotkey('ctrl', 's')
            
            # Ждем 2 секунды (важно! Окно "Сохранить как" открывается не мгновенно)
            time.sleep(2.0) 
            
            # 2. Жмем Enter (подтверждаем имя файла)
            pyautogui.press('enter')
            
            # Ждем 1.5 секунды, пока начнется скачивание
            time.sleep(1.5) 
            
            # 3. Закрываем вкладку, чтобы не считать их
            pyautogui.hotkey('ctrl', 'w')
            
            # Короткая пауза, чтобы следующая вкладка успела прогрузиться
            time.sleep(1.0) 
            
            print("Документ готов, закрываю вкладку...")

    except pyautogui.FailSafeException:
        pyautogui.alert("Скрипт остановлен. Вы молодец, идите пить кофе!")
    except Exception as e:
        pyautogui.alert(f"Произошла ошибка: {e}")

if __name__ == "__main__":
    main()